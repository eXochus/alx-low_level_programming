/**
 * op_add - perform addition
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - perform subtraction
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - perform multiplication
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - perform addition
 * @a: num1
 * @b: num2
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
